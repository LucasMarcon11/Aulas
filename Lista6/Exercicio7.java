/**
 *
 * Escreva uma classe em Java que imprima o quadrado dos números no intervalo de 1 a 20.
 */
public class Exercicio7 {

    public static void main(String[] args) {
        
        for (int i = 1; i <= 20; i++)
            System.out.println("O quadrado de " +i+ " é: " +(i * i));
    }
}
